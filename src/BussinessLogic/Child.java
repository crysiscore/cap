package BussinessLogic;

public class Child
{
  private String name;
  private String sex;
  private String birth_date;
  private String perm_id;
  private String moth_name;
  private String family_id;
  
  public String getName()
  {
    return this.name;
  }
  
  public void setName(String name)
  {
    this.name = name;
  }
  
  public String getSex()
  {
    return this.sex;
  }
  
  public void setSex(String sex)
  {
    this.sex = sex;
  }
  
  public String getBirth_date()
  {
    return this.birth_date;
  }
  
  public void setBirth_date(String birth_date)
  {
    this.birth_date = birth_date;
  }
  
  public String getPerm_id()
  {
    return this.perm_id;
  }
  
  public void setPerm_id(String perm_id)
  {
    this.perm_id = perm_id;
  }
  
  public String getMoth_name()
  {
    return this.moth_name;
  }
  
  public void setMoth_name(String moth_name)
  {
    this.moth_name = moth_name;
  }
  
  public String getFamily_id()
  {
    return this.family_id;
  }
  
  public void setFamily_id(String family_id)
  {
    this.family_id = family_id;
  }
}
